void DisPlayNum(uchar x2,uchar y2,uchar temp[])
{
	 unsigned i,j;
	 for(i=0;i<8;i++)
	 {
		 for(j=0;j<6;j++)
		 {
		 	Lcd_PutPixel(x2+j,y2+i,~(temp[i] & (1<< j))>>j);
		 }
	 }

}

void ChooseNum(uchar x1,uchar y1,uchar n)
{
	switch(n)
	{
		case 0 :  DisPlayNum(x1,y1,Num0); break;
		case 1 :  DisPlayNum(x1,y1,Num1); break;
		case 2 :  DisPlayNum(x1,y1,Num2); break;
		case 3 :  DisPlayNum(x1,y1,Num3); break;
		case 4 :  DisPlayNum(x1,y1,Num4); break;
		case 5 :  DisPlayNum(x1,y1,Num5); break;
		case 6 :  DisPlayNum(x1,y1,Num6); break;
		case 7 :  DisPlayNum(x1,y1,Num7); break;
		case 8 :  DisPlayNum(x1,y1,Num8); break;
		case 9 :  DisPlayNum(x1,y1,Num9); break;
		default:    break;  
	}
}
void DisPlayNums(uchar x,uchar y,uchar numbers)
{
	uchar num;
	if(numbers>=100)
	{
		num=numbers/100;
		ChooseNum(x,y,num);
		numbers=numbers%100;
	}
	if(numbers<100&&numbers>=10)
	{
		num=numbers/10;
		ChooseNum(x+6,y,num);
		numbers=numbers%10;
	}
	if(numbers<10)
	{
		ChooseNum(x+12,y,numbers);
	}
}
/***************************************
显示一个取模的字 12*12
***************************************/
void DisPlay(uchar x,uchar y,uchar temp[])
{
	 uchar i,j;
	 for(i =0 ;i<24;i++)
	 {
		  if(i<12)
		  {
		  		for(j=0; j<8;j++)
		 		{
		  			Lcd_PutPixel(x+j,y+i,~(temp[i] & (1<< j))>>j);
		 		}
	      }								   
		  else
		  {
				for(j=0 ;j<8 ;j++)
				{
						Lcd_PutPixel(x+j+8,y+i-12,~(temp[i] & (1<< j))>>j);
				}
		  }
	}
}

